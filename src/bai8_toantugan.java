public class bai8_toantugan {
    public static void main(String[] args) {
        //gắn cộng
        int x = 8;
        x+=5;
        System.out.println("x= "+x); //(x=x+5) = (x=+=5)

        //gắn trừ
        int x2=8;
        x2-=5; // x2=x2-5
        System.out.println("x2=" +x2);

        //gắn nhân
        int x3=8;
        x3*=5; // x3=x3*5
        System.out.println("x3=" +x3);

        int a=7;
        int b=9;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);


    }
}
